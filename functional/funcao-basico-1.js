// Function Declaration
function bomDia() {
    console.log('Bom dia!')
}
bomDia()


// Function Expression
const boaTarde = function() {
    console.log ('Boa Tarde!')
}
boaTarde() // Uma Expressão de Função é quando armazena a função em uma variável, em via de regra sempre será uma função anônima e a chamada da função será pela constante.

